# Description of a class
class MyClass:
    pass
# Creating objects based on the class
A=MyClass()
B=MyClass()
# Objects
print("Object A:",A)
print("Object B:",B)
# Object type
print("Object class A:",type(A).__name__)
print("Object class B:",type(B).__name__)
# Comparison of objects
print("A==B:",A==B)
